    public function getAssets(Request $request)
    {

        try{

            $data = [];

            $data['permission'] = [];
            $data['rows'] = config('vaahcms.per_page');

            $data['fillable']['columns'] = Lead::getFillableColumns();
            $data['fillable']['except'] = Lead::getUnFillableColumns();
            $data['empty_item'] = Lead::getEmptyItem();

            $lead_sttus=Taxonomy::getTaxonomyByType('lead-status');
            $data['status']=$lead_sttus->toArray();
            $data['users'] = EntitiesUser::all();
            $data['actions'] = [];

            $response['success'] = true;
            $response['data'] = $data;

        }catch (\Exception $e){
            $response = [];
            $response['success'] = false;
            if(env('APP_DEBUG')){
                $response['errors'][] = $e->getMessage();
                $response['hint'] = $e->getTrace();
            } else{
                $response['errors'][] = trans("vaahcms-general.something_went_wrong");
            }
        }

        return $response;
    }

